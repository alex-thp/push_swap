void	sa(int *a)
{
	int	c;

	c = a[0];
	a[0] = a[1];
	a[1] = c;
}

void	sb(int *b)
{
	int c;

	c = b[0];
	b[0] = b[1];
	b[1] = c;
}

void	ss(int *a, int *b)
{
	sa(a);
	sb(b);
}
